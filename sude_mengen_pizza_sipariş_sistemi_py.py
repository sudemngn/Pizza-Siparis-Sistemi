# Pizza decorator üst sınıf oluşturdum.
class PizzaDecorator(Pizza):
    def __init__(self, toppings):
        super().__init__()
        self.toppings = toppings

    def get_description(self):
        return self.toppings.get_description() + ' :' + Pizza.get_description(self)

    def get_cost(self):
        return self.toppings.get_cost() + ' =' + Pizza.get_cost(self)


