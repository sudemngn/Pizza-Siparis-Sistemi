# Pizza üst sınıf oluşturdum.
class Pizza:
    price: float
    def __init__(self):
        self.name = ""
        self.price = 0.0

    def get_description(self):
        return self.name
        Pizza.get_description(self)

    def get_cost(self):
        return self.price
        Pizza.get_cost(self)

